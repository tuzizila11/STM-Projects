/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  boolean_T eventFlags[2];

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_I2C1_Init();
  MX_TIM7_Init();
  /* USER CODE BEGIN 2 */
  /* Use Systick arm timer and interrupt to tick step() functions of the Simulink model. */
  /* Fundamental sample time is set to: '0.001000000' s */
  if (SysTick_Config((uint32_t)(SystemCoreClock/1000.0))) {
    autoReloadTimerLoopVal_S = 1;
    do {
      autoReloadTimerLoopVal_S++;
    } while ((uint32_t)(SystemCoreClock/1000.0)/autoReloadTimerLoopVal_S >
             SysTick_LOAD_RELOAD_Msk);

    SysTick_Config((uint32_t)(SystemCoreClock/1000.0)/autoReloadTimerLoopVal_S);
  }

  /* Set number of loop to do. */
  remainAutoReloadTimerLoopVal_S = autoReloadTimerLoopVal_S;

  {
    int i;
    for (i = 0; i < 2; i++) {
      OverrunFlags[i] = false;
    }
  }

  /* Initialize model */
  test_initialize();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  /* Infinite loop */
  /* Real time from systickHandler */
  while (1) {
    /*Process tasks every solver time*/
    if (remainAutoReloadTimerLoopVal_S == 0) {
      remainAutoReloadTimerLoopVal_S = autoReloadTimerLoopVal_S;

      /* Check base rate for overrun */
      if (OverrunFlags[0]) {
        rtmSetErrorStatus(test_M, "Overrun");
      }

      OverrunFlags[0] = true;

      /*
       * For a bare-board target (i.e., no operating system), the rates
       * that execute this base step are buffered locally to allow for
       * overlapping preemption.  The generated code includes function
       * writeCodeInfoFcn() which sets the rates
       * that need to run this time step.  The return values are 1 and 0
       * for true and false, respectively.
       */
      test_SetEventsForThisBaseStep(eventFlags);

      /* Step the model for base rate */
      test_step(0);

      /* Get model outputs here */

      /* Indicate task for base rate complete */
      OverrunFlags[0] = false;

      /* Step the model for any sub-rate */
      {
        int i;
        for (i = 1; i < 2; i++) {
          if (eventFlags[i]) {
            if (OverrunFlags[i]) {
              rtmSetErrorStatus(test_M, "Overrun");
            }

            OverrunFlags[i] = true;

            /* Step the model for sub-rate "i" */
            test_step(i);

            /* Get model outputs here */

            /* Indicate task complete for sample time "i" */
            OverrunFlags[i] = false;
          }
        }
      }
    }
  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  /* USER CODE END 3 */
}

